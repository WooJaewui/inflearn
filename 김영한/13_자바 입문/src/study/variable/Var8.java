package study.variable;

public class Var8 {
    public static void main(String[] args) {
        // 정수
        byte b = 127;                   // - 128 ~ 127
        short s = 32767;                // -32,768 ~ 32,767
        int i = 2147483647;             // 약 -21억 ~ 21억
        long l = 9223372036854775807L;  // +_ 2의 64승


        // 실수
        float f = 10.0f;
        double d = 10.0;
    }
}
